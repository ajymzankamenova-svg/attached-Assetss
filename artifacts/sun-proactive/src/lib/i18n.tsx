export function I18nProvider({ children }: { children: ReactNode }) {
  const { user } = useAuth();
  
  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem("sun_lang") as Language;
    return saved || "ru";
  });

  useEffect(() => {
    if (user?.language) {
      setLanguageState(user.language as Language);
      localStorage.setItem("sun_lang", user.language);
    }
  }, [user?.language]);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem("sun_lang", lang);
  };

  const t = (key: string, params?: Record<string, string | number>) => {
    let str = dictionary[language][key] || dictionary["ru"][key] || dictionary["en"][key] || key;
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        str = str.replace(`{{${k}}}`, String(v));
      });
    }
    return str;
  };

  return (
    <I18nContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (context === undefined) {
    throw new Error("useI18n must be used within an I18nProvider");
  }
  return context;
}
